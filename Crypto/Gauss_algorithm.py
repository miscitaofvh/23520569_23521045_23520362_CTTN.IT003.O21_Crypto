import numpy as np

def gauss_algorithm(v1, v2) -> list:
  while True:
    if(np.dot(v2, v2) < np.dot(v1, v1)):
      v1[0], v2[0] = v2[0], v1[0]
      v1[1], v2[1] = v2[1], v1[1]
    m = int(np.dot(v1, v2) / np.dot(v1, v1))

    if(m == 0):
      return [v1, v2]
    # v2 = v2 - m*v1
    v2[0] -= m*v1[0]
    v2[1] -= m*v1[1]
